  <h3 class="item-title">Előnyök</h3>
  <p>A cserepes tetőfedés hazánkban évszázados hagyományokra tekint vissza, s jelenleg is ez az egyik legelterjedtebb, legnépszerűbb tetőfedési módszer. Legnagyobb előnye, hogy kisebb elemekből áll össze, így felszerelése egyedi kialakítású tetők esetében is egyszerű.
</p>
  <div class="service-list">
  <ul>
    <li><i class="fas fa-check"></i>A tetőcserép egyik legnagyobb előnye, hogy kis elemekből épül fel, így felszerelése egyszerűbb egy nagyon tagolt vagy egyedi kialakítású tető esetében</li>
    <li><i class="fas fa-check"></i>A cserepek esetében igaz a tartósság, mely főleg abban nyilvánul meg, hogy a gyártók a cserép egészét színezik, így ha lepattan egy darab a cserépből, távolról ez az esztétikai hiba akkor sem észrevehető.</li>
    <li><i class="fas fa-check"></i>Tűzbiztonsági szempontból is jobb választás a tetőcserép helyett: acél anyaga nem éghető, speciális bevonata pedig megakadályozza a lángok terjedését.</li>
    <li><i class="fas fa-check"></i>Földrengésállóság tekintetében is jól teljesít: A betoncserepeknél, kerámiacserepeknél 7-szer könnyebb tető a szélsőséges viszonyoknak, kisebb földrengéseknek is ellenáll.</li>
  </ul>
